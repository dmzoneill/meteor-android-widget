package ie.MeteorWidget;

import ie.MeteorWidget.R;
import android.app.Activity;
import android.os.Bundle;
import android.text.method.ScrollingMovementMethod;
import android.widget.TextView;


public class MeteorAbout extends Activity
{


	@Override
	protected void onCreate( Bundle savedInstanceState )
	{
		super.onCreate( savedInstanceState );
		this.setContentView( R.layout.meteorabout_layout );

		TextView latest = ( TextView ) this.findViewById( R.id.aboutLatest );
		latest.setMovementMethod( new ScrollingMovementMethod() );
	}
}
